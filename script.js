// Counter animation for trust indicators
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start) + (element.dataset.suffix || '');
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target + (element.dataset.suffix || '');
        }
    }
    
    updateCounter();
}

// Trigger counter animation when trust indicators come into view
const trustObserver = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const counters = entry.target.querySelectorAll('.trust-number');
            counters.forEach(counter => {
                if (!counter.classList.contains('animated')) {
                    counter.classList.add('animated');
                    const target = parseInt(counter.textContent.replace(/\D/g, ''));
                    const suffix = counter.textContent.replace(/\d/g, '');
                    counter.dataset.suffix = suffix;
                    animateCounter(counter, target);
                }
            });
        }
    });
}, { threshold: 0.5 });

// Observe trust indicators
document.addEventListener('DOMContentLoaded', function() {
    const trustIndicators = document.querySelector('.trust-indicators');
    if (trustIndicators) {
        trustObserver.observe(trustIndicators);
    }
});

